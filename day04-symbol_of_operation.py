# Q1: Degrees Fahrenheit convert to Celsius.
# Hint function: C = (F - 32) / 1.8
f = float(input("Please input degrees Fahrenheit: "))
c = float((f-32)/1.8)
print("Degrees Celsius is : %.2f" % c)

